    private void dangNhapBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dangNhapBtnActionPerformed
        tenDangNhap = tenDangNhapTxt.getText();
        matKhau = matKhauTxt.getText();
        if (kiemTraNgoaiLeTaiKhoan(tenDangNhap, matKhau)) {
            if (kiemTraTonTaiTaiKhoan(tenDangNhap, matKhau)) {
                int idLoaiTK = getIDLoaiTaiKhoan(tenDangNhap, matKhau);
                // neu tai khoan la SV
                if (idLoaiTK == 1) {
                    KetNoiSQL_SV.setMssv(tenDangNhap);
                    MainFormSV mainForm = new MainFormSV(this);
                    this.setVisible(false);
                    mainForm.setVisible(true);
                } else if (idLoaiTK == 2) {
                    KetNoiSQL_GV.setMaGV(tenDangNhap);
                    MainFormGV mainForm = new MainFormGV(this);
                    this.setVisible(false);
                    mainForm.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, "Lỗi DataBase Không Lấy Được Dữ Liệu idLoaiTK",
                            "THÔNG BÁO", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_dangNhapBtnActionPerformed

