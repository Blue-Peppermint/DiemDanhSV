    private boolean kiemTraNgoaiLeTaiKhoan(String tenDangNhap, String matKhau) {
        int ngoaiLeTenDangNhap = getNgoaiLeTenDangNhap(tenDangNhap);
        if (ngoaiLeTenDangNhap == 1) {
            JOptionPane.showMessageDialog(this, "Tên Đăng Nhập Không Được Bỏ Trống",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (ngoaiLeTenDangNhap == 2) {
            JOptionPane.showMessageDialog(this, "Tên Đăng Nhập Tối Thiểu 5 Ký Tự",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (ngoaiLeTenDangNhap == 3) {
            JOptionPane.showMessageDialog(this, "Tên Đăng Nhập Ký Tự Đầu Tiên Không Thể Là Số",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (ngoaiLeTenDangNhap == 4) {
            JOptionPane.showMessageDialog(this, "Tên Đăng Nhập Ko Khoảng Trắng, Không Ký Tự Đặc Biệt",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        int ngoaiLeMatKhau = BasicAttribute_Method.getNgoaiLeMatKhau(matKhau);
        if (ngoaiLeMatKhau == 1) {
            JOptionPane.showMessageDialog(this, "Mật Khẩu Không Được Bỏ Trống",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (ngoaiLeMatKhau == 2) {
            JOptionPane.showMessageDialog(this, "Mật Khẩu Quá Dài",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

    private int getNgoaiLeTenDangNhap(String tenDangNhap) {
        if (tenDangNhap.equals("")) {
            return 1;
        }
        if (tenDangNhap.length() < 5) {
            return 2;
        }
        if (tenDangNhap.codePointAt(0) >= 48 && tenDangNhap.codePointAt(0) <= 57) {
            return 3;
        }
        String patternTenDangNhap = "^[a-zA-Z]([a-zA-Z0-9]+){4,20}";
        if (!tenDangNhap.matches(patternTenDangNhap)) {
            return 4;
        }
        return 0;
    }

    private int getIDLoaiTaiKhoan(String tenDangNhap, String matKhau) {
        // tra ve IDLoaiTK voi cac input tren
        // tim thay tra ve binh thuong
        // ko thay thi return -1;
        // nho dung prepareStatement de phong loi sql injection
        // Khi kiem tra ten dang nhap ko phan biet chu in hoa hay chu thuong (nhu trong UIS)
        int ketQua = -1;

        String user = "sa";
        String pass = "123";
        String url = "jdbc:sqlserver://localhost:1433;databaseName=CNPHANMEM3";
        Connection connection = null;

        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            connection = DriverManager.getConnection(url, user, pass);
            String sql = "SELECT IDLoaiTK FROM dbo.TaiKhoan WHERE [User]=? AND Pass=? ";
            PreparedStatement ps = connection.prepareStatement(sql);
            ps.setString(1, tenDangNhap);
            ps.setString(2, matKhau);

            ResultSet resultSet = ps.executeQuery();

            while (resultSet.next()) {
                ketQua = resultSet.getInt("IDLoaiTK");
            }

        } catch (Exception ex) {
            System.out.println(ex);
        } finally {
            if (connection != null) {
                try {
                    connection.close();
                } catch (SQLException ex) {
                    Logger.getLogger(LogInDialog.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

        }

        return ketQua;
    }

    private boolean kiemTraTonTaiTaiKhoan(String tenDangNhap, String matKhau) {
        boolean ketQua = BasicAttribute_Method.kiemTraTonTaiTaiKhoan(tenDangNhap, matKhau);
        if (ketQua == false) {
            JOptionPane.showMessageDialog(this, "Sai tên đăng nhập hoặc mật khẩu",
                    "THÔNG BÁO", JOptionPane.ERROR_MESSAGE);
        }
        return ketQua;
    }

